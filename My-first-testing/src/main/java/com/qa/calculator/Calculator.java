package com.qa.calculator;

public class Calculator {
	
	public int multiply(int num1, int num2) {
		return num1 * num2;
	}
	
	public int divsion(int num1, int num2) {
		return num1 / num2;

	}
	
	public int addition(int num1, int num2) {
		return num1 + num2;

	}
	
	public int subtraction(int num1, int num2) {
		return num1 - num2;

	}
}
